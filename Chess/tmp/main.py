def convertToCArray(piece):
    array = "int boardvalue_" + piece + "[64] = {\n"
    for row in reversed(getPieceMatrix(piece)):
        array += " " * 4
        for route in row:
            numstr = str(int(route))
            array += f"{' ' * (3 - len(numstr))}{numstr}, "
        array += "\n"
    array = array[:-3] + "\n};\n\n"
    return array


def writeAllValuesToFile(filename):
    with open(filename, "w") as f:
        f.write(convertToCArray("pawn"))
        f.write(convertToCArray("knight"))
        f.write(convertToCArray("bishop"))
        f.write(convertToCArray("rook"))
        f.write(convertToCArray("queen"))
        f.write(convertToCArray("king"))


if __name__ == '__main__':
    writeAllValuesToFile("pieceValues.txt")
